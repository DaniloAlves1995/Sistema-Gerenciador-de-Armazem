    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (this.manageFields.checkFields()) {
            SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd");
            //Cria um objeto product para setar os valores dos TextFilds
            Truck s = new Truck();
            
            //Seta os valores dos TextFilds
            s.setName(jTName.getText());
            s.setTruckLoad(Integer.parseInt(jTPreCarga.getText()));
            s.setData(df.format(new Date()));
            try {
                //Cria um objeto DAO para inserir o novo product no banco
                TruckDao d = new TruckDao();
                
                //Adiciona o novo product no banco
                d.add(s);
                int id = d.getLastId();
                s.setId(id);
                d.addTruckLoad(s);
                JOptionPane.showMessageDialog(null, "Truck registed with success!", "..: WMS :..", JOptionPane.INFORMATION_MESSAGE);
                
                //Limpa os TextFilds depois do Cadastro
                this.manageFields.clearFields();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Error to access the database! \n\r ERRORR:" + ex, "..: WMS :..", JOptionPane.ERROR_MESSAGE);
            }

        }

    }//GEN-LAST:event_jButton1ActionPerformed

